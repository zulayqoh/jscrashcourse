/**
 * Variables are like containers for any data type or structure
 */

// ES5
var yusuf;
// yusuf = "Yusuf";
console.log(yusuf);

// ES6
/**
 * let replaces var (but not really)
 */
let name; // definition
name = 'Gamaleil'; // assignment
console.log(name);

let surname = 'Onosode'; // definition and assignment
console.log(surname);

// const is used for constants
const PIE = 22.0/7;

console.log(PIE);
